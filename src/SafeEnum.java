enum Animal{
	DOG,CAT
}
enum Person{
	MAN, WOMAN
}

class SafeEnum {
	public static void main(String[] args) {
		who(Person.MAN);
		//who(Animal.DOG); 주석 해제하면 compile Error
	}
	public static void who(Person man) {
		switch(man) {
		case MAN:
			System.out.println("남성 손님입니다.");
			break;
		case WOMAN:
			System.out.println("여성 손님입니다.");
			break;
		}
	}
}
