class ArrayInstance {
	public static void main(String[] args) {
		int[] ar1 = new int[5];
		double[] ar2 = new double[7];
		float[] ar3;
		ar3 = new float[9];
		
		System.out.println("배열 ar1의 길이: "+ar1.length);
		System.out.println("배열 ar2의 길이: "+ar2.length);
		System.out.println("배열 ar3의 길이: "+ar3.length);
		
	}
}
